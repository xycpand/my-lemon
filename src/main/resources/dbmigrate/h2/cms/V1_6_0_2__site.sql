CREATE TABLE CMS_SITE(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
	TYPE VARCHAR(50),
	NAME VARCHAR(50),
	CODE VARCHAR(200),
	PRIORITY INT,
	TENANT_ID VARCHAR(64),
        CONSTRAINT PK_CMS_SITE PRIMARY KEY(ID)
);

